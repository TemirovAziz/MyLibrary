package com.example.mylibrary

class MyMath {
    companion object {
        fun addition(num1: Int, num2: Int): Int = num1 + num2
        fun subtraction(num1: Int, num2: Int): Int = num1 - num2
        fun divide(num1: Int, num2: Int): Int = num1 / num2
        fun mul(num1: Int, num2: Int): Int = num1 * num2
    }
}